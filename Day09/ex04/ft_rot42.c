char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if ((str[i] >= 'A' && str[i] <= 'P')
					|| (str[i] >= 'a' && str[i] <= 'p'))
			str[i] += 10;
		else if ((str[i] > 'P' && str[i] <= 'Z')
					|| (str[i] > 'p' && str[i] <= 'z'))
			str[i] -= 16;
		i++;
	}
	return (str);
}
