int		ft_is_prime(int nb)
{
	unsigned i;
	int check;

	i = 2;
	check = i;
	if (nb <= 1)
		return (0);
	while (i * i < nb  && nb > 1)
	{
		if ((nb % i) == 0)
			return (0);
		i++;
	}
	return (1);
}
